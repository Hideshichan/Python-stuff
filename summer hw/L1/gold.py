import turtle

screen = turtle.Screen()
turty = turtle.Turtle()

screen.bgcolor("white")
turty.color("yellow")
turty.pensize(5)

turty.forward(100)
turty.left(360/5)
turty.color("red")
turty.forward(100)
turty.left(360/5)
turty.color("green")
turty.forward(100)
turty.left(360/5)
turty.color("pink")
turty.forward(100)
turty.left(360/5)
turty.color("orange")
turty.forward(100)

screen.mainloop()